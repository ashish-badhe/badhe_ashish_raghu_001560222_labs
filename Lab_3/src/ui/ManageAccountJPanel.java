    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblAccountDirectory.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select account to delete.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete account ?", "Warning", dialogButton);
        
        if(dialogResult == JOptionPane.YES_OPTION){
                    
//            DefaultTableModel model = (DefaultTableModel)tblAccountDirectory.getModel();
            Account account = (Account)tblAccountDirectory.getValueAt(selectedRowIndex, 0);
        
            accountDirectory.deleteAccount(account);
        
//            JOptionPane.showMessageDialog(this, "Account deleted.");
        
            populateTable();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchAccountActionPerformed
        // TODO add your handling code here:
        
        int accountNumber;
        
        try{
            accountNumber = Integer.parseInt(txtSearch.getText());
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Invalid input!");
            return;
        }
        
        account = accountDirectory.searchAccount(accountNumber);
        
        if(account == null){
            JOptionPane.showMessageDialog(this, "Account does not exist.", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        
        SearchAccountJPanel searchPanel = new SearchAccountJPanel(userProcessContainer, account);
        userProcessContainer.add("SearchAccountJPanel", searchPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnSearchAccountActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblAccountDirectory.getSelectedRow();
        
        if(selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select account to view.");
            return;
        }
        
        
//        DefaultTableModel model = (DefaultTableModel)tblAccountDirectory.getModel();
        Account selectedAccount = (Account)tblAccountDirectory.getValueAt(selectedRowIndex, 0);
        

        
        ViewAccountJPanel viewAccount = new ViewAccountJPanel(userProcessContainer, selectedAccount, this, accountDirectory);
        userProcessContainer.add("ViewAccountJPanel", viewAccount);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        userProcessContainer.remove(this);
        
    }//GEN-LAST:event_btnViewActionPerformed


