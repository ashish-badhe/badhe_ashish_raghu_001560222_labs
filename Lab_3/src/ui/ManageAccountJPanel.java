    private void populateTable(){
                
        System.out.println(accountDirectory.getAccountDirectory());
        DefaultTableModel model = (DefaultTableModel)tblAccountDirectory.getModel();
        model.setRowCount(0);
        
        for(Account account : accountDirectory.getAccountDirectory()){
            Object[] row = new Object[4];
            row[0] = account;
            row[1] = account.getRoutingNumber();
            row[2] = account.getAccountNumber();
            row[3] = account.getBalance();

            model.addRow(row);
        }
    }

